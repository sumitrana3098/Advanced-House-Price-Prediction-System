@app.route("/predict", methods=["POST"])
def predict():

    area = int(request.form["area"])
    bedrooms = int(request.form["bedrooms"])
    bathrooms = int(request.form["bathrooms"])
    stories = int(request.form["stories"])
    parking = int(request.form["parking"])
    age = int(request.form["age"])
    locality_rating = int(request.form["locality_rating"])

    city = request.form["city"]
    furnishing = request.form["furnishing"]
    main_road = request.form["main_road"]
    guest_room = request.form["guest_room"]
    basement = request.form["basement"]
    water_supply = request.form["water_supply"]
    air_conditioning = request.form["air_conditioning"]
    preferred_tenant = request.form["preferred_tenant"]

    data = {
        "Area": area,
        "Bedrooms": bedrooms,
        "Bathrooms": bathrooms,
        "Stories": stories,
        "Parking": parking,
        "Age": age,
        "Locality Rating": locality_rating,

        "City_Chennai": 1 if city == "Chennai" else 0,
        "City_Delhi": 1 if city == "Delhi" else 0,
        "City_Hyderabad": 1 if city == "Hyderabad" else 0,
        "City_Kolkata": 1 if city == "Kolkata" else 0,
        "City_Mumbai": 1 if city == "Mumbai" else 0,
        "City_Pune": 1 if city == "Pune" else 0,

        "Furnishing_Semi-Furnished": 1 if furnishing == "Semi-Furnished" else 0,
        "Furnishing_Unfurnished": 1 if furnishing == "Unfurnished" else 0,

        "Main Road_Yes": 1 if main_road == "Yes" else 0,
        "Guest Room_Yes": 1 if guest_room == "Yes" else 0,
        "Basement_Yes": 1 if basement == "Yes" else 0,

        "Water Supply_Both": 1 if water_supply == "Both" else 0,
        "Water Supply_Corporation": 1 if water_supply == "Corporation" else 0,

        "Air Conditioning_Yes": 1 if air_conditioning == "Yes" else 0,

        "Preferred Tenant_Company": 1 if preferred_tenant == "Company" else 0,
        "Preferred Tenant_Family": 1 if preferred_tenant == "Family" else 0
    }

    input_df = pd.DataFrame([data])

    prediction = model.predict(input_df)[0]

# Price Category
    if prediction < 1000000:
       category = "Budget House"

    elif prediction < 1500000:
        category = "Mid-Range House"

    else:
        category = "Premium House"

    global latest_prediction

    latest_prediction = {
    "price": prediction,
    "category": category,

    "area": area,
    "bedrooms": bedrooms,
    "bathrooms": bathrooms,
    "stories": stories,
    "parking": parking,
    "age": age,
    "locality_rating": locality_rating,
    "city": city,
    "furnishing": furnishing
}

    return render_template(
    "index.html",
    prediction_text=f"Predicted House Price: ₹ {prediction:,.0f}",
    category=category
      )


    # return render_template(
    #     "index.html",
    #     prediction_text=f"Predicted House Price: ₹ {prediction:,.0f}"
    # )

if __name__ == "__main__":
    app.run(debug=True)